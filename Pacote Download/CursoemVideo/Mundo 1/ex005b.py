m = int(input(
    'Escolhe um numero ai:')
)
km = float(m * 0.001)
hm = m * 0.01
dam = m * 0.1
dm = m * 10
cm = m * 100
mm = m * 1000

print(
    f'Temos então que o número fica na tabela de medidas dessa forma:\n'
    f'{km}km\n'
    f'{hm}hm\n'
    f'{dam}dam\n'
    f'{m}m\n'
    f'{dm}dm\n'
    f'{cm}cm\n'
    f'{mm}mm'
)

